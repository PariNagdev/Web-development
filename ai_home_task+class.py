"""# Create Excel file with multiple sheets and than load 3rd sheet in Excel file"""

import pandas as pd

# Create example DataFrames for each sheet
df1 = pd.DataFrame({'PassengerId':[1,2,3,4], 'Name':['Alice','Bob','Charlie','Diana'], 'Gender':['F','M','M','F'], 'Age':[25,30,35,28]})
