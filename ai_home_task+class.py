"""# How do I access data in a dataframe? (Explore loc and iloc options)

In Pandas, loc and iloc are the primary methods for selecting data from a DataFrame. The fundamental difference is that loc is label-based (using names), while iloc is integer-based (using positions).
"""

# Access multiple columns
print(df[['Age', 'Fare']])

# Access single column
print(df['Age'])

# Access rows 0 to 5
#also called as slicing
print(df[0:5])

# Using loc (label-based)
print(df.loc[0:5, ['Age','Fare']])

# Using iloc (position-based)
print(df.iloc[0:5, 2:5])

# Access a single row by index label
df.loc[0]

